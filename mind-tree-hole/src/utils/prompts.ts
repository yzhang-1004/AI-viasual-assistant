/**
 * 根据模式生成 System Prompt
 * @param mode 当前模式
 * @param visionContext 视觉上下文文本（如 "[视觉上下文：环境昏暗，用户面容疲惫]"）
 */
export function buildSystemPrompt(mode: AppMode, visionContext: string): string {
  const base = BASE_PROMPTS[mode]
  const visionPart = visionContext
    ? `\n\n[系统提示] 你们正在进行视频通话，你可以通过摄像头看到用户。以下是当前画面信息的实时摘要，如果用户问你能否看到他们，请根据这些信息自然回应——不要说你"看不到"：\n${visionContext}`
    : ''
  return base + visionPart
}

