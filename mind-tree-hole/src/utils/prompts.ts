/**
 * 生成 L0 规则引擎的快速回应（ZERO 成本）
 */
export function getL0Response(userText: string): string | null {
  const text = userText.trim()

  // 纯语气词
  if (/^(嗯|啊|哦|呃|唔|哎|唉|诶)+$/.test(text)) return '嗯，我在听。'

  // 打招呼
  if (/^(你好|嗨|嘿|hello|hi|在吗|在不在)$/i.test(text)) return '嗨，我在呢。今天想聊点什么？'

  // 简短倾诉
  if (/^(我好(累|烦|难过|伤心|焦虑|害怕|孤独|无聊))$/.test(text)) {
    const feeling = text.replace('我好', '')
    return `听起来你感到${feeling}，我在这里陪着你。`
  }

  // 感谢
  if (/^谢(谢|了)/.test(text)) return '不客气，能陪着你我很高兴。'

  return null
}
